package com.emirates.dash.model;

public class HaulType {

	private String ODCountry;
	private String origin;
	private String destination;
	private String haul;
	
	public String getODCountry() {
		return ODCountry;
	}
	public void setODCountry(String oDCountry) {
		ODCountry = oDCountry;
	}
	public String getOrigin() {
		return origin;
	}
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	public String getDestination() {
		return destination;
	}
	public void setDestination(String destination) {
		this.destination = destination;
	}
	public String getHaul() {
		return haul;
	}
	public void setHaul(String haul) {
		this.haul = haul;
	}
	
	
	
	
}
